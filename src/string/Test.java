package string;

public class Test {
public static void main(String[]args) {
	
	String s="Tester";
	System.out.println(s.toUpperCase());        //TESTER
	System.out.println(s.toLowerCase());        //tester
	System.out.println(s.indexOf("s"));         //2
	System.out.println(s.charAt(5));             //r
	System.out.println(s.startsWith("Tes"));      //true
	System.out.println(s.endsWith("terr"));       //false
	System.out.println(s.lastIndexOf("t"));       //3
	System.out.println(s.length());               //6
	
	
	
	
	
	
}
}
